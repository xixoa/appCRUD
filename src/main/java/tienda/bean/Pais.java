
package tienda.bean;

public class Pais {
    private int idpais;
    private String  npais;

    public Pais(String npais) {
        this.npais = npais;
    }

    public Pais() {
    }

    public int getIdpais() {
        return idpais;
    }

    public void setIdpais(int idpais) {
        this.idpais = idpais;
    }

    public String getNpais() {
        return npais;
    }

    public void setNpais(String npais) {
        this.npais = npais;
    }
    
    
        
    
}
